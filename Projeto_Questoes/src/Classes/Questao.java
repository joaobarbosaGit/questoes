
package Classes;


public class Questao {
    
    private int idQuestao;
    private String Corpo_Questao;
    private String Gabarito;
    private String Qualificacao_Questao;

    public int getIdQuestao() {
        return idQuestao;
    }

    public void setIdQuestao(int idQuestao) {
        this.idQuestao = idQuestao;
    }

    public String getCorpo_Questao() {
        return Corpo_Questao;
    }

    public void setCorpo_Questao(String Corpo_Questao) {
        this.Corpo_Questao = Corpo_Questao;
    }

    public String getGabarito() {
        return Gabarito;
    }

    public void setGabarito(String Gabarito) {
        this.Gabarito = Gabarito;
    }

    public String getQualificacao_Questao() {
        return Qualificacao_Questao;
    }

    public void setQualificacao_Questao(String Qualificacao_Questao) {
        this.Qualificacao_Questao = Qualificacao_Questao;
    }
    
    
    
}
