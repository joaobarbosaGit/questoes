
package Classes;

public class Resposta {
    
    private int idResposta;
    private String Corpo_Resposta;
    private String Nota_Resposta;

    public int getIdResposta() {
        return idResposta;
    }

    public void setIdResposta(int idResposta) {
        this.idResposta = idResposta;
    }

    public String getCorpo_Resposta() {
        return Corpo_Resposta;
    }

    public void setCorpo_Resposta(String Corpo_Resposta) {
        this.Corpo_Resposta = Corpo_Resposta;
    }

    public String getNota_Resposta() {
        return Nota_Resposta;
    }

    public void setNota_Resposta(String Nota_Resposta) {
        this.Nota_Resposta = Nota_Resposta;
    }

    
}
